package com.heima.bean;

public class Cat {

	private String name ;
	
	private String color ;
	
	public Cat() {
	}

	public Cat(String name, String color) {
		super();
		this.name = name;
		this.color = color;
	}

	public String getName() {
		return name;
	}

	public String getColor() {
		return color;
	}
}
